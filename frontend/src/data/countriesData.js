// Helper function to get countries list (for backward compatibility)
export const getCountriesList = () => countriesWithPhoneCodes.map(c => c.name);

// Helper function to get phone codes list with flags
export const getPhoneCountryCodes = () => 
  countriesWithPhoneCodes.map(c => ({
    code: c.dialCode,
    label: `${c.flag} ${c.dialCode}`,
    country: c.name
  }));

// Helper function to get phone code for a country
export const getPhoneCodeByCountry = (countryName) => {
  const country = countriesWithPhoneCodes.find(c => c.name === countryName);
  return country ? country.dialCode : '+1';
};

// Helper function to get country by phone code
export const getCountryByPhoneCode = (dialCode) => {
  const country = countriesWithPhoneCodes.find(c => c.dialCode === dialCode);
  return country ? country.name : null;
};

